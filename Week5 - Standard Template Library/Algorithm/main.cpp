//* ALGORITHMS
/*
? Functional Library
_ Defines templated function-objects (functors) that can be passed as arguments
to the other functions, including the algorithm templates
<functional> 
+ Wrapper Class Template: Manage access to some underlying object.
+ Function Template: Binds one or more arguments to a functor
+ Operator Class Template: Define functor equivalents for most of the operator present in the core

? Algorithm Library
_ Perform common operations on ranges of elements in a sequence. The function calls accept
these ranges as arguments in the form of iterators.
<algorithm>
+ Queries
+ Modifier
+ Manipulators

? Numeric Library 
_ Provides standard templated functions for performing numeric operations on ranges of elements in a sequence

*/

